package applica.framework.library.mail;

public class MailException extends Exception {

    /**
     *
     */
    private static final long serialVersionUID = 7223192435924601545L;

    public MailException() {
        super();
    }

    public MailException(String arg0, Throwable arg1) {
        super(arg0, arg1);
    }

    public MailException(String arg0) {
        super(arg0);
    }

    public MailException(Throwable arg0) {
        super(arg0);
    }


}
